package com.geek.lesson37;

public class Coord {
    private Float cord1;
    private Float cord2;

    public Coord(Float cord1, Float cord2) {
        this.cord1 = cord1;
        this.cord2 = cord2;
    }

    public Float getCord1() {
        return cord1;
    }

    public Float getCord2() {
        return cord2;
    }
}
